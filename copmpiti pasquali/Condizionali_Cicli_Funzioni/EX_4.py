def print_seq():
    print("Sequenza a):")
    for i in range (1, 8):
        print(i)
    print()
    
    print("Sequenza b):")
    numero_b = 3
    for _ in range (5):
        print(numero_b)
        numero_b += 5
    print()
    
    print("Sequenza c):")
    for numero_c in range(20, -12, -6):
        print(numero_c)
    print()
    
    print("Sequenza d):")
    for numero_d in range(19, 52, 8):
        print(numero_d)
print_seq()





