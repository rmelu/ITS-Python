quadrato = lambda x: x**2

# Esempio di test
numero = 5
risultato = quadrato(numero)
print(f"Il quadrato di {numero} è: {risultato}")

# Puoi testare con altri numeri
print(f"Il quadrato di 10 è: {quadrato(10)}")
print(f"Il quadrato di 0 è: {quadrato(0)}")
print(f"Il quadrato di -3 è: {quadrato(-3)}")