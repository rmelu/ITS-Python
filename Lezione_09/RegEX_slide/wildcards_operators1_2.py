'''Wildcards
• . — Dot (Any character)
Matches any single character except newline (\n) by default.
Example: a.c matches "abc", "axc", "a1c", but not "ac" or "abbc".
• .* — Zero or more of any character
Matches any number of any characters, including none.
Example: a.*c matches "ac", "abc", "axyzc", etc.
• .+ — One or more of any character
Matches at least one of any character.
Example: a.+c matches "abc", "axyzc", but not "ac"
'''



